            Photo tips for Coastal Texas
        </h3>
    </a>
    <p>
        Photography along the warm, humid Gulf coast is quite a lot different from the middle of the country, or even most other coastal areas.
        It's flatter and muddier, so the geology isn't helping much. It wants to be an overgrown forest, so a lot of it is very closed in. 
        It rains - a lot, and heavily - which creates challenges ranging from keeping your gear dry to keeping your car out of the flood waters. 
        But catch it at the right moment, when the weather gods are on your side, and it can be beautiful, interesting, and exciting, worth every bit of the effort you put into it.
